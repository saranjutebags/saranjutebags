export const getRoadmapStats = (roadmap = adminFeatureRoadmap) => {
  const counts = { done: 0, partial: 0, pending: 0, total: 0 };

  roadmap.forEach((module) => {
    module.items.forEach((item) => {
      counts.total += 1;
      counts[item.status] += 1;
    });
  });

  return counts;
};
